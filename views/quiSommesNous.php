                    <h2>Qui est Flow at work ?</h2>
                    <p>Flow at Work est une entreprise qui se concentre sur l'analyse et la mesure prédictive du bien-être au travail, moteur de l'équilibre, de l'engagement et de la fidélité de vos collaborateurs. Cette solution s'appuie sur mes travaux de recherche et intègre des aspects cognitifs, émotionnels, sociaux et organisationnels.<img src="../images/meditation.jpg" alt="Image 1"></p><br><br>


                    <h2>Processus</h2>
                    <p>Dans un premier temps, nous réalisons un audit en examinant vos pratiques actuelles en matière de gestion du bien-être psychologique de vos salariés. Nous mènerons ensuite également des recherches approfondies pour comprendre les besoins et les attentes spécifiques de votre personnel. Ensuite, grâce à notre plateforme en ligne, nous mettrons en place des questionnaires numériques scientifiques et des entretiens pour recueillir les ressentis de vos salariés sur leur expérience de travail actuelle. Ces données nous permettront de comprendre les facteurs de leur bien-être optimal et leur capacité à atteindre l'état de flow en contexte de travail. Sur la base des résultats obtenus, nous effectuerons un diagnostic précis de votre entreprise et développerons des plans d'action sur mesure pour améliorer le bien-être des salariés. Ces plans d'action pourront inclure des interventions organisationnelles, un aménagement du temps de travail, des formations, des activités de renforcement des compétences émotionnelles et sociales et du coaching. Nous travaillerons en étroite collaboration avec nos partenaires et votre entreprise pour mettre en œuvre ces plans d'action et assurer leur suivi. Nous fournirons des outils numériques pour faciliter le suivi et l'évaluation des progrès réalisés dans la gestion du bien-être des salariés.<img src="../images/Processus.jpg" alt="Image 1"></p><br><br>

                    <h2>Projections</h2>
                    <p>En choisissant Flow At Work, vous vous engagez à créer un environnement de travail sain et propice à l'épanouissement de vos salariés et à l'amélioration globale de la performance de votre entreprise. Notre approche basée sur la perspective sociale conative vous permettra d'optimiser l'expérience de travail de vos salariés, augmentant ainsi leur engagement, leur satisfaction, leur fidélité et leur productivité. Investir précocement dans un diagnostic proactif du bien être optimal de vos collaborateurs garantirala productivité future de votre entreprise. Chaque euro investi génère un retour de 4 euros. Flow At Work s'engage à améliorer les indicateurs clés de performance des ressources humaines tels que l'absentéisme, le turnover, l'engagement et le Net Promoter Score. Nous vous remercions pour votre confiance !<br><br><img src="../images/../images/projection.jpg" alt="Image 1">
                    </p><br><br>

                    <h2>Perspectives</h2>
                    <p>D'ici 2030, nous pouvons nous attendre à ce que les applications et les plateformes de e-santé mentale soient devenues encore plus sophistiquées et personnalisées. Les algorithmes et l'intelligence artificielle seront utilisés pour fournir des recommandations et des interventions spécifiques à chaque individu, en fonction de leurs besoins uniques.<img src="../images/../images/perpective.png" alt="Image 1">
